    void progressBarStart () {
        timer1 = new javax.swing.Timer(100, new ActionListener() {
            private int i=0;
            public void actionPerformed(ActionEvent e) {
                i++;
                updateValue(i);
            }
        });
    }
     void updateValue (int value) {
        if (value <= 100) {
            jProgressBar.setValue(value);
        }
        else{
            timer1.stop();
            this.setVisible(false);
            new Login();
        }
    }


